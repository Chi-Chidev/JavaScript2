document.getElementById('salary1').onclick = function () {

    const position = document.getElementById('position1').value.toLowerCase();

    let salaryMessage;

    if (position === "manager") {
        salaryMessage = "This employee's salary is $5,000.";
    } else if (position === "developer") {
        salaryMessage = "This employee's salary is $3,000.";
    } else if (position === "intern") {
        salaryMessage = "This employee's salary is $1,000.";
    } else {
        salaryMessage = "Invalid position. Please enter 'Manager', 'Developer', or 'Intern'.";
    }  

    alert(salaryMessage);
}

